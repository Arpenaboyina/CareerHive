function postedDate(daysAgo) {
  const d = new Date();
  d.setDate(d.getDate() - daysAgo);
  return d.toISOString();
}

// Enrich each raw record with derived, presentation-ready fields.
export const jobs = rawJobs.map((job, index) => {
  const id = String(index + 1);
  return {
    ...job,
    id,
    currency: "USD",
    logoColor: BRAND_COLORS[index % BRAND_COLORS.length],
    logoInitials: initials(job.company),
    postedAt: postedDate(job.postedDaysAgo),
    salaryUnit: job.type === "Internship" ? "month" : "year",
  };
});

export function getJobById(id) {
  return jobs.find((job) => job.id === String(id));
}

export function getCategoryLabel(id) {
  const found = CATEGORIES.find((c) => c.id === id);
  return found ? found.label : id;
}

export function getCategoryCounts() {
  return CATEGORIES.map((category) => ({
    ...category,
    count: jobs.filter((job) => job.category === category.id).length,
  }));
}

export default jobs;
